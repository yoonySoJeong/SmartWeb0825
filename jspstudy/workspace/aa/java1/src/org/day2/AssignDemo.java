package org.day2;

public class AssignDemo {

	public static void main(String[] args) {
		int a=3, b=3, c=3;
		
		a+=3; /* a=a+3 6  ; a=+3으로 적으면 계산 안됨 */
		b*=5; /* b=b*5 15 */
		c%=2; /* c=c/2  1 */
		
		System.out.println("a="+a);
		System.out.println("b="+b);
		System.out.println("c="+c);
		
		
		
	}

}
